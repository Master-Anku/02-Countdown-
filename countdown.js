// Write code to print all numbers from `num` down to 1
// You may assume `num` will be a positive number

var countdown = function(num) {
  // num ဆိုတဲ့နံပါတ်ကနေ စတင်ဖို့ဖြစ်ပါတယ်။
  for (let i = num; i >= 1; i--) {
    // i နံပါတ်ကို console မှာ ပြပါ။
    console.log(i);
};
};

